# Функция для преобразования температуры из градусов Цельсия в Фаренгейта
def celsius_to_fahrenheit(c):
    # Формула перевода из Цельсия в Фаренгейт: F = C × 9/5 + 32
    f = (c * 9 / 5) + 32
    # Возвращаем полученное значение
    return f

# Запрашиваем у пользователя температуру в градусах Цельсия
celsius = float(input("Введите температуру в градусах Цельсия: "))

# Преобразуем введенную температуру в градусы Фаренгейта
fahrenheit = celsius_to_fahrenheit(celsius)

# Выводим результат с точностью до двух знаков после запятой
print(f"{celsius}°C эквивалентно {fahrenheit:.2f}°F")