# Функция для проверки, является ли число простым.
def is_prime(number):
    # Если число меньше 2, оно не может быть простым (простое число должно быть больше 1).
    if number < 2:
        return False
    
    # Проверяем делители числа от 2 до квадратного корня из числа включительно.
    for i in range(2, int(number**0.5) + 1):
        # Если находим хотя бы один делитель, значит число составное.
        if number % i == 0:
            return False

    # Если ни одного делителя не найдено, то число простое.
    return True

# Считываем ввод пользователя.
n = int(input("Введите число: "))

# Выводим результат проверки.
if is_prime(n):
    print(f"{n} — простое число")
else:
    print(f"{n} — не простое число")