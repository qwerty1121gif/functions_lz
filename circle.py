import math  # Импортируем модуль math для использования математических функций

r = float(input("Введите радиус круга: "))  # Получаем радиус круга от пользователя

# Вычисляем площадь круга по формуле πr²
circarea = math.pi * r ** 2  

# Вычисляем длину окружности по формуле 2πr
circlen = 2 * math.pi * r  

# Выводим результаты с округлением до двух знаков после запятой
print(f"Площадь круга с радиусом {r:.2f}: {circarea:.2f}")
print(f"Длина окружности с радиусом {r:.2f}: {circlen:.2f}")